import Foundation
import UIKit

extension UIColor {
    static func primaryBackground() -> UIColor {
        return UIColor(named: "PrimaryBackground")!
    }
    
    static func secondaryBackground() -> UIColor {
        return UIColor(named: "SecondaryBackground")!
    }
}
